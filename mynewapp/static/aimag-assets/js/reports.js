function remove_letter(a) {
    return a.replace(/\D/g, "");
} //remove_letter
function add_delimiter(a) {
    last2 = a.slice(-2);
    aa = a.substring(0, a.length - 2);
    aa = parseInt(aa);
    aa = numberWithCommas(aa) + "." + last2;
    return aa;
} // add_delimiter

function currency_formatter_e(event) {
    var aa = event.value;
    aa = "000" + aa;
    aa = remove_letter(aa);
    aa = add_delimiter(aa);
    event.value = aa;
}

function allow_integer(event) {
    let startPos = event.selectionStart;
    let endPos = event.selectionEnd;
    let length = event.value.length;
    let lastLen = length - startPos;
  
    event.value = event.value
      .replace(/[^0-9]/g, "")
      .replace(/(\..*)\./g, "$1");
    if (event.value != "") {
      event.value = parseInt(event.value).toFixed(0);
    }
    event.value = numberWithCommas(event.value);
    event.setSelectionRange(
      event.value.length - lastLen,
      event.value.length - lastLen
    );
  } //allow_integer


  function allow_float(event) {
    let startPos = event.selectionStart;
    let endPos = event.selectionEnd;
    let length = event.value.length;
    let lastLen = length - startPos;
  
    event.value = event.value
      .replace(/[^0-9.]/g, '')
      .replace(/(\..*?)\..*/g, '$1');
  
    if (event.value != "") {
      event.value = parseFloat(event.value).toFixed(2);
    }
    event.value = numberWithCommas(event.value);
    event.setSelectionRange(
      event.value.length - lastLen,
      event.value.length - lastLen
    );
  } //allow_float
  
  function numberWithCommas(x) {
    return x.toString().replace(/\B(?<!\.\d*)(?=(\d{3})+(?!\d))/g, ",");
  } //numberWithCommas

