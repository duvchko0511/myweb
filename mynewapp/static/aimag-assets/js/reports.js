function set_val_default(event, tZero) {
    if (tZero == 0) {
      if (event.value == "") {
        event.value = "0";
      }
    } else if (tZero == 1) {
      if (event.value == "") {
        event.value = "0.0";
      }
    } else {
      if (event.value == "") {
        event.value = "0.00";
      }
    }
    } //set_val_default

